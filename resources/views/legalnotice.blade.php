						  A legal notice is the medium which informs the receiver about the legal proceedings initiated against him/her. It is a formal warning which needs immediate action on receiving. Serving a legal notice notifies the other party that you are suing them, gives them the reasons why you are suing them, and provides the other side with the legal basis upon which you filed the lawsuit.  The other party then has a certain time period to respond, usually 30 days. <br>
Legal notices are a vital principal of the courts providing fairness and due process by giving all parties affected by the lawsuit or legal proceeding notice of the legal procedure. No party can operate in secret and all court actions must be apparent to all parties to the case
Legal notices therefore are extremely important.  They inform a party that they are being sued and serve as an indication that this party now needs to take action to defend oneself. <br> 
The legal notice must contain:<br>
<ul>
<li>  Name of the parties and the court resolving the dispute</li>
<li>  Name and address of plaintiff and any attorneys the plaintiff has</li>
<li>  Time the defendant should appear at court</li>
<li>  Notice that failure to appear at the prearranged time will result in a default judgment against the defendant (the plaintiff automatically wins)</li>
<li>  The court’s seal and a clerk’s signature</li></ul>
Depending on the type of legal suit or proceeding you are in need of, we provide you with expert help in drafting your legal notice.


